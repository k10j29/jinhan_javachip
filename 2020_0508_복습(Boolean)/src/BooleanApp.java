
public class BooleanApp {

	public static void main(String[] args) {

		// String, Integer

		System.out.println("ONE"); // String (문자)
		System.out.println(1); // Integer (정수)

		// Boolean

		System.out.println(true);
		System.out.println(false);

		String foo = "Hello World";
		// String true = "Hllo World"; reserved word

		System.out.println(foo.contains("World")); // World 를 포함한다 안한다 (한다)
		System.out.println(foo.contains("Jinhan")); // 한다 안한다

	}

}
