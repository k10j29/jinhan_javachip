class Ex_while1 
{
	public static void main(String[] args) 
	{
		//for:반복횟수를 알때 사용
		int i= 0;//초기식
		for( ;i<3; ){
		   System.out.print("안녕");
		   i++;
		}

        System.out.println("\n--------------");

        //while:반복횟수를 모를때 사용 
		int n = 0;
		while(n<3){
           System.out.print("안녕");
		   n++;
		}




	}
}
